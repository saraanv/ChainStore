    /*edit info wanted*/
    private void editBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editBtnMouseClicked
       /*if empty it hints*/
        if(catId.getText().isEmpty()||catName.getText().isEmpty()||catDes.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Select To Edit") ;
        }
        /*it will connect too related database and change info wanted*/
        else{
            try{
                /*address of database*/
                Con=DriverManager.getConnection("jdbc:derby://localhost:1527/TheChainStore","admin1","admin");
               /*finds where database is called and will update it*/
                String Query="Update admin1.CATEGORY set CATNAME='"+catName.getText()+"'"+",CATDES='"+catDes.getText()+"'"+"where CATID="+catId.getText();
                Statement Add = Con.createStatement();
                Add.executeUpdate(Query) ;
                /*when it is shown the method is done successfully*/
                JOptionPane.showMessageDialog(this,"Edited") ;
                selectBranch();
            }catch(SQLException e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_editBtnMouseClicked


    /*to add a new category*/
    private void addBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addBtnMouseClicked
        /*if empty it will hints*/
        if(catId.getText().isEmpty() ||catName.getText().isEmpty()||catDes.getText().isEmpty ())
        {
            JOptionPane.showMessageDialog(this,"Fill The Buttons!") ;
        }
        /* it will connect to database and add info given by indexes*/
        else{
            try{
                /*connects to related database and insert from table chosen and indexes*/
                Con=DriverManager.getConnection("jdbc:derby://localhost:1527/TheChainStore","admin1","admin");
                PreparedStatement add = Con.prepareStatement("insert into CATEGORY values(?,?,?)");
                add.setInt(1 , Integer.valueOf(catId.getText()));
                add.setString(2, catName.getText());
                add.setString(3,catDes.getText());
                int row = add.executeUpdate();
                  /*when it is shown the method is done successfully*/
                JOptionPane.showMessageDialog(this,"Added Successfully") ;
                /*to add the info given instantly to the table*/
                Con.close();
                selectBranch();

            } catch(SQLException e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_addBtnMouseClicked

    /*for deleting a category*/
    private void deleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteBtnMouseClicked
        /*if empty it will hint*/
        if(catId.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Select To Delete!") ;
        }
        /*connects to dtabase and deletes choosen one*/
        else{
            try{
                /*connects to related database and will delet the given address to it*/
                Con=DriverManager.getConnection("jdbc:derby://localhost:1527/TheChainStore","admin1","admin");
                String catgId =catId.getText();
                String Query="Delete from admin1.CATEGORY WHERE CATID="+catgId ;
                Statement Add = Con.createStatement();
                Add.execute(Query);
                selectBranch();
                /*when it is shown the method is done successfully*/
                JOptionPane.showMessageDialog(this,"Deleted!") ;
            }
            catch(SQLException e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_deleteBtnMouseClicked

